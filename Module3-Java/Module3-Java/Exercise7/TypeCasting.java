/*Task7-TypeCasting */
public class TypeCasting{
  public static void main(String[] args){
    double mydbl=9.001;
    int n=(int) mydbl;
    System.out.println("The double value is:"+mydbl);
    System.out.println("Double to Int conveted value is:"+n);
    int num=6;
    double d=num;
    System.out.println("The integer value is:"+num);
    System.out.println("Int to Double converted value is:"+d);
  }
}